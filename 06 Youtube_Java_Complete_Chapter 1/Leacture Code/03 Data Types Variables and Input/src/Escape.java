public class Escape {
    public static void main(String[] args) {
        System.out.println("Hello 'KG Coding'... ");
        System.out.println("Hello \'KG Coding\'...");
        System.out.println("Hello \"KG Coding\"...");
        System.out.println("Hello \\KG Coding\\...");
        System.out.println("Hello \nKG Coding...");
        System.out.println("Hello\tKGCoding...");
        System.out.println("Hello\bKGCoding...");

    }
}
